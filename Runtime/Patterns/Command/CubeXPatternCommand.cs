using System;
using System.Collections.Generic;
using System.ComponentModel;
//=====================================================================================================================
namespace CubeX
{
	namespace Core
	{
		//-------------------------------------------------------------------------------------------------------------
		//! \defgroup CorePatternCommand Паттерн команда
		//! Паттерн команда обеспечивает механизм хранения действия(метода) в отдельном объекте и с связанных с ним 
		//! элементов (источника команды, цели команды и т.д.). 
		//! \ingroup CorePattern
		/*@{*/
		//-------------------------------------------------------------------------------------------------------------
		/// <summary>
		/// Интерфейс команды управления
		/// </summary>
		//-------------------------------------------------------------------------------------------------------------
		public interface ICubeXCommand
		{
			#region ======================================= МЕТОДЫ ====================================================
			//---------------------------------------------------------------------------------------------------------
			/// <summary>
			/// Исполнение команды
			/// </summary>
			//---------------------------------------------------------------------------------------------------------
			void Execute();

			//---------------------------------------------------------------------------------------------------------
			/// <summary>
			/// Отмена исполнения команды
			/// </summary>
			//---------------------------------------------------------------------------------------------------------
			void UnExecute();
			#endregion
		}
		//-------------------------------------------------------------------------------------------------------------
		/*@}*/
		//-------------------------------------------------------------------------------------------------------------
	}
}
