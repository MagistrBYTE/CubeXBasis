namespace CubeX
{
	namespace Core
	{
		//-------------------------------------------------------------------------------------------------------------
		//! \defgroup CoreModuleExtension Методы расширений
		//! Общие методы расширения для базовых типов платформы NET и Unity.
		//! В подсистеме приведены только общие методы расширения базовых типов платформы NET и Unity направленных на 
		//! унификацию функциональных возможностей. Специфичные методы расширения будут реализованы в соответствующих 
		//! подсистемах.
