using System;
using System.Collections;
using System.Reflection;
//=====================================================================================================================
namespace CubeX
{
	namespace Core
	{
		//-------------------------------------------------------------------------------------------------------------
		//! \addtogroup CoreModuleInspectorCollection
		/*@{*/
		//-------------------------------------------------------------------------------------------------------------
		/// <summary>
		/// Атрибут для определения колонки отображения данных дочернего поля/свойства объекта
		/// </summary>
		//-------------------------------------------------------------------------------------------------------------
		[AttributeUsage(AttributeTargets.Property | AttributeTargets.Field, Inherited = false, AllowMultiple = false)]
		public class CubeXColumnAttribute : Attribute
		{
			#region ======================================= ДАННЫЕ ====================================================
			internal readonly Single mPercent;
			#endregion

			#region ======================================= СВОЙСТВА ==================================================
			/// <summary>
			/// Процент занимаемой ширины
			/// </summary>
			public Single Percent
			{
				get { return (mPercent); }
			}
			#endregion

			#region ======================================= КОНСТРУКТОРЫ ==============================================
			//---------------------------------------------------------------------------------------------------------
			/// <summary>
			/// Конструктор инициализирует объект класса указанными параметрами
			/// </summary>
			/// <param name="percent">Процент занимаемой ширины</param>
			//---------------------------------------------------------------------------------------------------------
			public CubeXColumnAttribute(Single percent)
			{
				mPercent = percent;
			}
			#endregion
		}
		//-------------------------------------------------------------------------------------------------------------
		/*@}*/
		//-------------------------------------------------------------------------------------------------------------
	}
}
